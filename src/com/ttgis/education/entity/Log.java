package com.ttgis.education.entity;

public class Log {
    private String logId;
    
    private String logUser;
    
    private String logContent;
    
    private String logAt;
    
    private String logIp;
    
    private String logUserType;
    
    private int size;
    
    private int page;
    
    private String startDate;
    
    private String endDate;

	public String getLogId() {
		return logId;
	}

	public void setLogId(String logId) {
		this.logId = logId;
	}

	public String getLogUser() {
		return logUser;
	}

	public void setLogUser(String logUser) {
		this.logUser = logUser;
	}

	public String getLogContent() {
		return logContent;
	}

	public void setLogContent(String logContent) {
		this.logContent = logContent;
	}

	public String getLogAt() {
		return logAt;
	}

	public void setLogAt(String logAt) {
		this.logAt = logAt;
	}

	public String getLogIp() {
		return logIp;
	}

	public void setLogIp(String logIp) {
		this.logIp = logIp;
	}

	public String getLogUserType() {
		return logUserType;
	}

	public void setLogUserType(String logUserType) {
		this.logUserType = logUserType;
	}

	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}

	public int getPage() {
		return page;
	}

	public void setPage(int page) {
		this.page = page;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
    
    
   
}